                    <h4>G.&nbsp; Polyps</h4>
                    <p>The adenocarcinoma can arise in adenomatous (tubular, tubulovillous, or villous) or serrated (sessile serrated adenoma/polyp or traditional serrated adenoma) polyp. Sessile serrated adenoma often develops cytologic dysplasia resembling tubular adenoma during neoplastic progression. These are presumed to be the precursors of right-sided adenocarcinomas with high levels of microsatellite instability (MSI-H).<sup>40</sup></p>
                    <p>&nbsp;</p>
                    <h4>H.&nbsp; Perforation</h4>
                    <p>Tumor perforation is an uncommon complication of colorectal cancer, but one that is associated with a poor outcome, including high in-hospital mortality and morbidity.<sup>41</sup> Perforation of the uninvolved colon proximal to an obstructing tumor is also associated with high mortality because of generalized peritonitis and sepsis. Reported perforation rates range from 2.6% to 9%. Perforation is more likely to occur in older patients.</p>
                    <p>&nbsp;</p>
                    <h5>I.&nbsp; Mesorectal Envelope</h5>
                    <p>The quality of the surgical technique is a key factor in the success of surgical treatment for rectal cancer, both in the prevention of local recurrence and in long-term survival. Numerous studies have demonstrated that total mesorectal excision (TME) improves local recurrence rates and the corresponding survival by as much as 20%. This surgical technique entails precise sharp dissection within the areolar plane outside (lateral to) the visceral mesorectal fascia to remove the rectum. This plane encases the rectum, its mesentery, and all regional nodes and constitutes Waldeyer&rsquo;s fascia. High-quality TME surgery reduces local recurrence from 20% to 30%, to 8% to 10% or less, and increases 5-year survival from 48% to 68%.<sup>42,43</sup> Adjuvant therapy in the presence of a high-quality TME may further reduce local recurrence (from 8% to 2.6%).<sup>44</sup></p>
                    <p>&nbsp;</p>
                    <p>Pathologic evaluation of the resection specimen has been shown to be a sensitive means of assessing the quality of rectal surgery. It is superior to indirect measures of surgical quality assessment, such as perioperative mortality, rates of complication, number of local recurrences, and 5-year survival. Macroscopic pathologic assessment of the completeness of the mesorectum, scored as complete, partially complete, or incomplete, accurately predicts both local recurrence and distant metastasis.<sup>43</sup> <sup>&nbsp;</sup>Microscopic parameters, such as the status of the circumferential resection margin, the distance between the tumor and nearest circumferential margin (ie,&nbsp;&ldquo;surgical clearance&rdquo;), and the distance between the tumor and the closest distal margin, are all important predictors of local recurrence and may be affected by surgical technique.</p>
                    <p>&nbsp;</p>
                    <p>The nonperitonealized surface of the fresh specimen is examined circumferentially, and the completeness of the mesorectum is scored as described below.<sup>43</sup> The entire specimen is scored according to the worst area.</p>
                    <p>&nbsp;</p>
                    <h5>Incomplete</h5>
                    <ul>
                    <li>Little bulk to the mesorectum</li>
                    <li>Defects in the mesorectum down to the muscularis propria</li>
                    <li>After transverse sectioning, the circumferential margin appears very irregular</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>Nearly Complete</h5>
                    <ul>
                    <li>Moderate bulk to the mesorectum</li>
                    <li>Irregularity of the mesorectal surface with defects greater than 5 mm, but none extending to the muscularis propria</li>
                    <li>No areas of visibility of the muscularis propria except at the insertion site of the levator ani muscles</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>Complete</h5>
                    <ul>
                    <li>Intact bulky mesorectum with a smooth surface</li>
                    <li>Only minor irregularities of the mesorectal surface</li>
                    <li>No surface defects greater than 5 mm in depth</li>
                    <li>No coning towards the distal margin of the specimen</li>
                    <li>After transverse sectioning, the circumferential margin appears smooth</li>
                    </ul>
                    <h4>J. Margins </h4>
                    <p>It may be helpful to mark the margin(s) closest to the tumor with ink following close examination of the serosal surface for puckering and other signs of tumor involvement. Margins marked by ink should be designated in the macroscopic description of the surgical pathology report. The serosal surface (visceral peritoneum) does not constitute a surgical margin.</p>
                    <p>&nbsp;</p>
                    <p>In addition to addressing the proximal and distal margins, the radial margin (Figure 3A-3C) must be assessed for any segment either unencased (Figure 3C) or incompletely encased by peritoneum (Figure 3B) (see Note A). The radial margin represents the adventitial soft tissue margin closest to the deepest penetration of tumor and is created surgically by blunt or sharp dissection of the retroperitoneal or subperitoneal aspect, respectively. Since the lower rectum is entirely extraperitoneal, the radial margin extends circumferentially and has been referred to as the circumferential radial margin. Multivariate analysis has suggested that tumor involvement of the circumferential (radial) margin is the most critical factor in predicting local recurrence in rectal cancer.<sup>44</sup> A positive circumferential (radial) margin in rectal cancer increases the risk of recurrence by 3.5-fold and doubles the risk of death from disease. For this reason, the circumferential (radial) margin should be assessed in all rectal carcinomas as well as colonic segments with nonperitonealized surfaces. The circumferential (radial) margin is considered negative if the tumor is more than 1 mm from the inked nonperitonealized surface but should be recorded as positive if tumor is located 1 mm or less from the nonperitonealized surface, because local recurrence rates are similar with clearances of 0 to 1 mm. There is limited outcome data for cases with intranodal or intravascular tumor within 1 mm of radial resection margin, but follow-up based on a small number of patients suggests that local recurrence in these tumors may be similar to those with negative margin.<sup>44,45</sup></p>
                    <p>&nbsp;</p>
                    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; A &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; B&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; C</p>
