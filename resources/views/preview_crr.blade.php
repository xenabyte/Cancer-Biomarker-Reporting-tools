                    </p>
                    <p>
                        For rectal cancers, metastasis in the external iliac or common iliac nodes
                        is classified as distant metastasis.<sup>1</sup>
                    </p>
                    <p>
                        <u>Submission of Lymph Nodes for Microscopic Examination. </u>
                        All grossly negative or equivocal lymph nodes should be submitted entirely.
                        Grossly positive lymph nodes may be partially submitted for microscopic
                        confirmation of metastasis.
                    </p>
                    <p>
                        The accuracy and predictive value of stage II assignment are directly
                        proportional to the thoroughness of the surgical technique in removing all
                        regional nodes and the pathologic examination of the resection specimen in
                        identifying and harvesting all regional lymph nodes for microscopic
                        assessment. The National Quality Forum lists the presence of at least 12
                        lymph nodes in a surgical resection among the key quality measures for
                        colon cancer care in the United States (see
                        http://www.facs.org/cancer/qualitymeasures.html).
                    </p>
                    <p>
                        The likelihood of detecting metastasis increases with the number of lymph
                        nodes examined; hence 12 lymph nodes should be considered the minimum
                    target, but all possible lymph nodes should be retrieved and examined.    <sup>54,55</sup>
                    </p>
                    <p>
                    The clinical outcome is linked to lymph node harvest in stage II disease,    <sup> 56</sup> indicating a positive effect of optimal mesenteric resection
                        by the surgeon, optimal lymph node harvest from the resection specimen by
                        the pathologist, or both.
                    </p>
                    <p>
                        The number of lymph nodes recovered from a resection specimen is dependent
                        on several factors. Surgical technique, surgery volume, and patient factors
                        (eg, age and anatomic variation) alter the actual number of nodes in a
                        resection specimen, but the diligence and skill of the pathologist in
                        identifying and harvesting lymph nodes in the resection specimen also are
                        major factors. Lymph nodes may be more difficult to identify in specimens
                        from patients who are obese<sup>57</sup> or elderly, or after neoadjuvant
                        therapy.<sup>58 </sup>Because it has been shown that nodal metastasis in
                        colorectal cancer is often found in small lymph nodes (&lt;5 mm in
                        diameter), diligent search for lymph nodes is required on gross examination
                        of resection specimens. If fewer than 12 lymph nodes are found,
                        re-examining the specimen for additional lymph nodes, with or without
                        visual enhancement techniques, should be considered. The pathology report
                        should clearly state the total number of lymph nodes examined and the total
                        number involved by metastases. Data are insufficient to recommend routine
                        use of tissue levels or special/ancillary techniques.
                    </p>
                    <p>
                        <u>Nonregional Lymph Nodes</u>
                        . For microscopic examination of lymph nodes in large resection specimens,
                        lymph nodes must be designated as regional versus nonregional, according to
                        the anatomic location of the tumor. Metastasis to nonregional lymph nodes
                        is classified as distant metastasis and designated as M1.
                    </p>
                    <p>
                        <u>Lymph Nodes Replaced by Tumor</u>
                        . A tumor nodule in the pericolonic/perirectal fat without histologic
                        evidence of residual lymph node tissue is classified as a tumor deposit
                        (peritumoral deposit or satellite nodule) and is not considered a positive
                        lymph node. In the absence of unequivocal lymph node metastases, tumor
                        deposits are recorded as N1c.<sup>1</sup>
                    </p>
                    <p>
                        <u>Isolated Tumor Cells</u>
                        . Isolated tumor cells (ITCs) are defined as single tumor cells or small
                        clusters of tumor cells measuring 0.2 mm or less, usually found by special
                    techniques such as immunohistochemical staining, and are classified as N0.    <sup>1</sup> Because the biologic significance of ITCs (either a single
                        focus in a single node, multiple foci within a single or multiple nodes)
                        remains unproven, N0 is considered justified.<sup>59</sup> The number of
                        lymph nodes involved by ITCs should be clearly stated in a comment section
                        or elsewhere in the report. Metastatic deposits 0.2 mm-2.0 mm have been
                        referred to as micrometastasis. These nodes should be considered as
                        involved by cancer. A separate designation of micrometastasis (N1mi) can be
                        used, but is not necessary.<sup>1</sup>
                    </p>
                    <p>
                        Routine assessment of regional lymph nodes is limited to conventional
                        pathologic techniques (gross assessment and histologic examination), and
                        data are currently insufficient to recommend special measures to detect
                        ITCs. Thus, neither multiple levels of paraffin blocks nor the use of
                        special/ancillary techniques such as immunohistochemistry are recommended
                        for routine examination of regional lymph nodes.<sup> </sup>
                    </p>
                    <h2>
                        TNM Anatomic Stage/Prognostic Groupings
                    </h2>
                    <p>
                        Pathologic staging is usually performed after surgical resection of the
                        primary tumor. Pathologic staging depends on pathologic documentation of
                        the anatomic extent of disease, whether or not the primary tumor has been
                        completely removed. If a biopsied tumor is not resected for any reason (eg,
                        when technically unfeasible), and if the highest T and N categories or the
                        M1 category of the tumor can be confirmed microscopically, the criteria for
                        pathologic classification and staging have been satisfied without total
                        removal of the primary cancer.
                    </p>
                    <ol>
                    <h4><strong> Ancillary Studies</strong></h4>
                    </ol>
                    <p>Universal testing for microsatellite instability and/or status DNA mismatch repair enzymes by immunohistochemistry is recommended by the EGAPP guidelines.<sup>60,61</sup> The NCCN guidelines also advocate this approach for patients &lt;70 years. MSI-high cancers are associated with right-sided location, tumor infiltrating lymphocytes, Crohn-like infiltrate, pushing borders, mucinous/signet ring/medullary subtypes, intratumoral heterogeneity (mixed conventional, mucinous, and poorly differentiated carcinoma), high-grade histology, and lack of dirty necrosis.<sup>62,63</sup> In view of recommendations for universal testing and chance of missing cases of Lynch syndrome with testing based on Bethesda guidelines,<sup>63</sup> evaluation of histologic features associated with MSI is not required and is no longer included in the synoptic comment.</p>
                    <p>&nbsp;</p>
                    <p>Since MSI-H cancers have a favorable prognosis, MSI testing for stage II cases can help in making decisions regarding adjuvant chemotherapy. Since MSI-H cancers do not respond well to 5-FU therapy, MSI status is also important in determining the choice of chemotherapeutic regimen.<sup>64</sup> &nbsp;</p>
                    <p>&nbsp;</p>
                    <p>Further details about mismatch repair enzyme immunohistochemistry and PCR for MSI testing, as well as other mutation testing in colorectal cancer (such as <em>KRAS</em>, <em>BRAF</em>) can be found in the CAP Colon and Rectum Biomarkers protocol.</p>
