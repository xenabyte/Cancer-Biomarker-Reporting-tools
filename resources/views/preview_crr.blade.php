                    <p>&nbsp;</p>
                    <p>The mesenteric resection margin (&lsquo;vascular tie&rsquo; margin) is the only relevant &lsquo;radial&rsquo; margin in segments completely encased by peritoneum (eg, transverse colon). Involvement of this margin should be reported even if tumor does not involve the serosal surface.</p>
