                    <h2>L.&nbsp; Tumor Deposits</h2>
                    <p>A tumor focus in the pericolic/perirectal fat or in adjacent mesentery (mesocolic or rectal fat) within the lymph drainage area of the primary tumor, but without identifiable lymph node tissue or vascular structure. If the vessel wall or its remnant is identified (H&amp;E, elastic, or any other stain), it should be classified as vascular (venous) invasion, and not as tumor deposit. Similarly, a tumor focus is present in or around a large nerve, should be classified as perineural invasion and not as tumor deposit. Size and shape of the tumor focus are not relevant for classification as a tumor deposit.</p>
                    <p>&nbsp;</p>
                    <p>The presence of tumor deposits in the absence of any regional node involvement is categorized as N1c, irrespective of T category. Tumor deposits are an adverse prognostic factor<sup>49,50</sup> and adjuvant therapy is generally warranted in cases that are categorized as N1c regardless of T classification. The number of tumor deposits should be recorded. If tumor deposits are accompanied by&nbsp;identifiable&nbsp;lymph node metastasis (including micrometastasis), it does not affect the N category, which is then determined by the number of positive lymph nodes (see note M).</p>
                    <p>&nbsp;</p>
                    <p>In the setting of preoperative or neoadjuvant therapy, the designation of tumor deposit should be used with caution as the tumor foci may represent residual primary tumor with incomplete response. &nbsp;</p>
                    <h4 >Pathologic Stage Classification (pTNM, AJCC 8th Edition)</h4>
                    <p>
                        Surgical resection remains the most effective therapy for colorectal
                        carcinoma, and the best estimation of prognosis is derived from the
                        pathologic findings on the resection specimen. The anatomic extent of
                        disease is by far the most important prognostic factor in colorectal
                        cancer.
                    </p>
                    <p>
                        The protocol recommends the TNM staging system of the American Joint
                        Committee on Cancer (AJCC) and the International Union Against Cancer
                        (UICC)<sup>1</sup> but does not preclude the use of other staging systems.
                    </p>
                    <p>
                        By AJCC/UICC convention, the designation “T” refers to a primary tumor that
                        has not been previously treated. The symbol “p” refers to the pathologic
                        classification of the TNM, as opposed to the clinical classification, and
                        is based on gross and microscopic examination. pT entails a resection of
                        the primary tumor or biopsy adequate to evaluate the highest pT category,
                        pN entails removal or biopsy of nodes adequate to validate lymph node
                        metastasis, and pM implies microscopic examination of distant lesions.
                        Clinical classification (cTNM) is usually carried out by the referring
                        physician before treatment during initial evaluation of the patient or when
                        pathologic classification is not possible.
                    </p>
                    <h2>
                        TNM Descriptors
                    </h2>
                    <p>
                        For identification of special cases of TNM or pTNM classifications, the “m”
                        suffix and “y” and “r” prefixes are used. Although they do not affect the
                        stage grouping, they indicate cases needing separate analysis.
                    </p>
                    <p>
                        <u>The “m” suffix</u>
                        indicates the presence of multiple primary tumors in a single site and is
                        recorded in parentheses: pT(m)NM.
                    </p>
                    <p>
                        <u>The “y” prefix</u>
                        indicates those cases in which classification is performed during or
                        following initial multimodality therapy (ie, neoadjuvant chemotherapy,
                        radiation therapy, or both chemotherapy and radiation therapy). The cTNM or
                        pTNM category is identified by a “y” prefix. The ycTNM or ypTNM categorizes
                        the extent of tumor actually present at the time of that examination. The
                        “y” categorization is not an estimate of tumor prior to multimodality
                        therapy (ie, before initiation of neoadjuvant therapy).
                    </p>
                    <p>
                        <u>The “r” prefix</u>
                        indicates a recurrent tumor when staged after a documented disease-free
                        interval, and is identified by the “r” prefix: rTNM.
                    </p>
