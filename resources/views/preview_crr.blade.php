                        The regional lymph nodes for the anatomical subsites of the large intestine
                        (Figure 7) are as follows:
                    </p>
                    <p>
                        <u>Cecum: </u>
                    </p>
                    <p>
                        Pericolic, ileocolic, right colic
                    </p>
                    <p>
                        <u>Ascending colon</u>
                        :
                    </p>
                    <p>
                        Pericolic, ileocolic, right colic, right branch of middle colic
                    </p>
                    <p>
                        <u>Hepatic flexure</u>
                        :
                    </p>
                    <p>
                        Pericolic, ileocolic, middle colic, right colic
                    </p>
                    <p>
                        <u>Transverse colon</u>
                        :
                    </p>
                    <p>
                        Pericolic, middle colic
                    </p>
                    <p>
                        <u>Splenic flexure</u>
                        :
                    </p>
                    <p>
                        Pericolic, middle colic, left colic
                    </p>
                    <p>
                        <u>Descending colon</u>
                        :
                    </p>
                    <p>
                        Pericolic, left colic, inferior mesenteric, sigmoid
                    </p>
                    <p>
                        <u>Sigmoid colon</u>
                        :
                    </p>
                    <p>
                    Pericolic, sigmoid, inferior mesenteric, superior rectal (hemorrhoidal)    <u>Rectosigmoid</u>: Pericolic, sigmoid, superior rectal (hemorrhoidal)
                    </p>
                    <p>
                        <u>Rectum</u>
                        :
                    </p>
                    <p>
                        Mesorectal, superior rectal (hemorrhoidal), inferior mesenteric, internal
                        iliac, inferior rectal (hemorrhoidal)
                    </p>
                    <br clear="all"/>
                    <p>
                        <img src="reporting_images/crr8.png" class="img-responsive" /><br/>
                        <strong>Figure 7.</strong>
                        The regional lymph nodes of the colon and rectum. Used with permission of
                        the American Joint Committee on Cancer (AJCC), Chicago, Ill. The original
