                        Tumor that is adherent to other organs or structures macroscopically is
                        classified clinically as cT4. However, if no tumor is found within the
                        adhesion microscopically, the tumor should be assigned pT3.<sup>1</sup>
                    </p>
                    <p>
                        For rectal tumors, invasion of the external sphincter and/or levator ani
                        muscle(s) is classified as T4b.
                    </p>
                    <p>
                        Tumor in veins or lymphatics does not affect the pT classification.
                    </p>
                    <p>
                        <u>Subdivision of T4 into T4a and T4b</u>
                        . Serosal (visceral peritoneal) involvement by tumor cells (pT4a) has been
                        demonstrated by multivariate analysis to have a negative impact on
                        prognosis,<sup>50,51 </sup>as does direct invasion of adjacent organs
                        (pT4b). Visceral peritoneal involvement can be missed without thorough
                        sampling and/or sectioning, and malignant cells have been identified in
                        serosal scrapings in as many as 26% of specimens categorized as pT3 by
                        histologic examination alone.<sup>52,53</sup> Although the absence of
                        standard guidelines for assessing peritoneal involvement may contribute to
                        underdiagnosis, the following findings are considered to represent serosal
                        involvement by tumor:
                    </p>
                    <ul type="disc">
                        <li>
                            Tumor present at the serosal surface
                        </li>
                        <li>
                            Free tumor cells on the serosal surface (on the visceral peritoneum)
                            with underlying erosion/ulceration of mesothelial lining, mesothelial
                            hyperplasia and/or inflammatory reaction<sup>51,52</sup>
                        </li>
                    </ul>
                    <p>
                        · Perforation in which the tumor cells are continuous with the serosal
                        surface through inflammation
                    </p>
                    <p>
                        The significance of tumors that are &lt;1 mm from the serosal surface and
                        accompanied by serosal reaction is unclear, with some<sup>52</sup> but not
                        all studies<sup>51</sup> indicating a higher risk of peritoneal recurrence.
                        Multiple level sections and/or additional section of the tumor should be
                        examined in these cases. If the serosal involvement is not present after
                        additional evaluation, the tumor should be assigned to the pT3 category.
                        The use of elastic stains has been advocated for identification of T4a
                        tumors by demonstrating tumor involvement of the subperitoneal elastic
                        lamina. The elastic stain can be difficult to interpret in this region and
                        the elastic lamina is not uniformly present even in normal colon. Hence
                        routine use of this stain is not considered standard practice. In portions
                        of the colorectum that are not peritonealized (eg, posterior aspects of
                        ascending and descending colon, lower portion of rectum), the T4a category
                        is not applicable.
                    </p>
                    <p>
                        Direct invasion in T4 includes invasion of other organs or other segments
                        of the colorectum as a result of direct extension through the serosa, as
                        confirmed on microscopic examination (for example, invasion of the sigmoid
                        colon by a carcinoma of the cecum) or, for cancers in a retroperitoneal or
                        subperitoneal location, direct invasion of other organs or structures by
                        virtue of extension beyond the muscularis propria (i.e., respectively, a
                        tumor on the posterior wall of the descending colon invading the left
                        kidney or lateral abdominal wall; or a mid or distal rectal cancer with
                        invasion of prostate, seminal vesicles, cervix, or vagina).
                    </p>
                    <p>
                        Tumor that is adherent to other organs or structures, grossly, is
                        classified cT4b. However, if no tumor is present in the adhesion,
                        microscopically, the classification should be pT1-4a depending on the
                        anatomical depth of wall invasion. The V and L classifications should be
                        used to identify the presence or absence of vascular or lymphatic invasion
                        whereas the PN prognostic factor should be used for perineural invasion.
                    </p>
                    <p>
                        Intramural extension of tumor from one subsite (segment) of the large
                        intestine into an adjacent subsite or into the ileum (eg, for a cecal
                        carcinoma) or anal canal (eg, for a rectal carcinoma) does not affect the
                        pT classification. Transmural extension into another organ or site is
                        necessary for T4b designation.
                    </p>
                    <p>
                        Both types of peritoneal involvement are associated with decreased
                        survival. Although small studies suggested that serosal involvement was
                        associated with worse outcome than invasion of adjacent organs, data from a
                        large cohort of more than 100,000 colon cancer cases<sup>53</sup> indicate
                        that penetration of the visceral peritoneum carries a 10% to 20% better
                        5-year survival than locally invasive carcinomas for each category of N.
                    </p>
