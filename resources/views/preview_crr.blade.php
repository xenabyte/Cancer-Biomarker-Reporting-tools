                    <p>Sections to evaluate the proximal and distal resection margins can be obtained either by longitudinal sections perpendicular to the margin or by en face sections parallel to the margin. The distance from the tumor edge to the closest resection margin(s) may also be important, particularly for low anterior resections. For these cases, a distal resection margin of 2 cm is considered adequate; for T1 and T2 tumors, 1 cm may be sufficient distal clearance. Anastomotic recurrences are rare when the distance to the closest margin is 5 cm or greater.</p>
                    <p>&nbsp;</p>
                    <p>In cases of carcinoma arising in a background of inflammatory bowel disease, proximal and distal resection margins should be evaluated for dysplasia and active inflammation. Proximal, distal, and radial/mesenteric resection margins should&nbsp;be reported in all resections specimens. Deep margin and mucosal margins should be reported in all transanal disk excisions.</p>
